    <p>Poštovani,</p>
    U tijeku je praćenje provedbe preporuka. Molimo vas da nas izvijestite o aktivnostima koje su poduzete u cilju provedbe preporuka. Očitovanje o poduzetim aktivnostima dostaviti na mail: <a href="mailto:unutarnja.revizija@lutrija.hr">unutarnja.revizija@lutrija.hr</a><br>
    Ako je koja od preporuka u međuvremenu provedena molim da dostavite/učitate dokaze o provedbi iste u aplikaciju „Unutarnja revizija“.<br>
    Također, molimo da obratite pozornost na rokove za provedbu preporuka iz Plana djelovanja, a sukladno Zakonu o sustavu unutarnjih kontrola u javnom sektoru:</p>
    <h3>Odgovornost za provedbu i praćenje provedbe preporuka unutarnje revizije, članak 34.:</h3>
    <ol>
        <li>Odgovorna osoba revidirane jedinice odgovorna je za provedbu preporuka iz Plana djelovanja.</li>
        <li>O statusu provedbe preporuka odgovorna osoba revidirane jedinice obvezna je izvijestiti rukovoditelja unutarnje revizije koji prati njihovu provedbu i izvještava o provedbi preporuka odgovornu osobu institucije, odnosno odbor, gdje je primjenjivo.</li>
    </ol>
    <h3>Mjere u slučaju neprovođenja preporuka unutarnje revizije korisnika proračuna, članak 35.:</h3>
    <ol>
        <li>Ako preporuke nisu provedene prema Planu djelovanja, odgovorna osoba revidirane jedinice obvezna je u izvješću iz članka 34. stavka 2. ovoga Zakona obrazložiti zašto preporuke nisu provedene te u dogovoru s rukovoditeljem unutarnje revizije predložiti novi rok provedbe i aktivnosti.</li>
        <li>Rok provedbe preporuka iz stavka 1. ovog članka može se produžiti samo jednom, o čemu odluku donosi odgovorna osoba institucije.</li>
        <li>Ako odgovorna osoba revidirane jedinice utvrdi da određene preporuke nisu provedive, obvezna je u izvješću iz članka 34. stavka 2. ovoga Zakona to izričito navesti te detaljno obrazložiti zašto preporuke nisu provedive.</li>
        <li>Rukovoditelj unutarnje revizije na temelju izvješća iz članka 34. stavka 2. ovoga Zakona obavještava odgovornu osobu institucije o provedbi preporuka.</li>
        <li>Odgovorna osoba institucije u vezi s preporukama iz stavka 3. ovoga članka, a na prijedlog rukovoditelja unutarnje revizije, donosi odluku o:<br>
            a) Neprovedivosti preporuke ili<br>
            b) Ponovnom obavljanju revizije u roku ne dužem od godine dana od podnošenja izvješća iz stavka 4. ovoga članka.
        </li>
    </ol>
    <p>S poštovanjem,<br>Unutarnja revizija</p>
